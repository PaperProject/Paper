  <p><h2><b>Stage en entreprise</b></h2>
  La durée du stage est de 6 semaines. La période du stage, identique pour tous les élèves d'une même promotion, se situe en fin de première année scolaire (mai/juin).

  Le stage doit s'effectuer dans une entreprise susceptible d’employer un titulaire du diplôme et dont l'activité est en adéquation avec le référentiel. Ceci correspond principalement à trois types d’entreprises :
  <ul class="liste">
    <li>&rarr; Les sociétés de services en informatique industrielle,</li>
    <li>&rarr; Les sociétés qui installent, utilisent, maintiennent des équipements automatisés ou informatisés,</li>
    <li>&rarr; Les sociétés qui fabriquent des équipements automatisés ou informatisés ou des solutions dédiées.</li>
  </ul>
  Le rapport de stage en entreprise est utilisé principalement comme support de l'épreuve d'économie et de gestion.

  <h2><b>Rapport d'activités</b></h2>
  L'étudiant rédige au jour le jour son rapport d'activités. Les documents produits utilisent le standard de présentation en vigueur dans l'entreprise. Il y intègre notamment :
  <ul class="liste" >
    <li>&rarr; La présentation de l'entreprise,</li>
    <li>&rarr; Le travail confié au stagiaire,</li>
    <li>&rarr; La consignation des travaux effectués.</li>
  </ul>Le rapport, épuré de toute information confidentielle, ne contient que des données techniques et des informations économiques communiquées par l'entreprise, avec son aval. C'est à partir de ce document que sera bâti le rapport de stage.

  <h2><b>Rapport de stage</b></h2>
  Du rapport d'activités, l'étudiant extrait, structure et analyse les informations qui lui permettent de constituer le rapport de stage. Le dossier comprend deux parties de poids sensiblement équivalent :
  La première, associée à l'enseignement "économie et gestion", contient les documents
          concernant :
  <ul class="liste">
      <li>&rarr; L’organisation de l’entreprise.</li>
      <li>&rarr; Les activités de l’entreprise et leur analyse économique.</li>
      <li>&rarr; Les aspects économiques (marché, approvisionnement, coûts, etc.).</li>
      <li>&rarr; Ses partenaires extérieurs (clients, fournisseurs, sous-traitants, etc.).</li>
      <li>&rarr; Les relations avec les partenaires extérieurs.</li>
      <li>&rarr; Les relations humaines dans l’entreprise.</li>
      <li>&rarr; La seconde, liée au domaine professionnel, contient :</li>
      <li>&rarr; La nature de l'activité (ou des activités) confiée(s) à l’étudiant.</li>
      <li>&rarr; Les réflexions et les conclusions tirées à l'issue du stage.</li>
      <li>&rarr; Les éléments de solutions apportés aux problèmes techniques posés.</li>
  </ul>Dans cette partie, l'étudiant s'attache à montrer l'intégration de son activité dans celles de l'entreprise.</li>
  
  Le rapport de stage est établi en 5 exemplaires :
  <ul class="liste" >
    <li>&rarr; Trois pour les membres du jury,</li>
    <li>&rarr; Un pour l'équipe pédagogique,</li>
    <li>&rarr; Un pour l'étudiant.</li>
  </ul>
  Les dossiers seront remis au jury au cours du mois de février de la deuxième année.
  
  <h2><b>Épreuves (coefficient 2)</b></h2>
  Le rapport de stage en entreprise est utilisé comme support de l'épreuve orale de communication professionnelle. La soutenance dure 30 minutes. Le jury est composé de deux professeurs d'anglais et d'économie et gestion.
  L'oral est organisé en trois parties :
  <ul class="liste">
    <li>&rarr; une présentation personnelle du candidat et de son stage en langue anglaise pendant 5 minutes.</li>
    <li>&rarr; un entretien de 15 minutes en langue anglaise sur tous les aspects du stage</li>
    <li>&rarr; un entretien de 10 minutes en langue française sur une pratique professionnelle de l'entreprise de stage.</li>
  </ul>
  </p>
  
  
  
